package type;

public class TypeCast {
    public static void main(String[] args) {
        String a = "a" + 3;
        System.out.println(a);
        float b = 5 + 5.0f;
        System.out.println(b);
        double d = 2.0d + 2;
        System.out.println(d);
        int i = 24 % 7;
        System.out.println(i);
        int k = 100 << 2;
        System.out.println(k);
        int j = 100 << 2;
        System.out.println(j);
    }
}
